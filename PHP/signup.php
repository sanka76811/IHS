        });


        $(window).resize(function(){
            setFooter();    
        });

        function setFooter(){
            if($('.page-wrapper').height() < $(window).height()){
                $('.page-wrapper').addClass("full-height");
                $('.main-footer').addClass("absolute");
            }
            else{
                $('.page-wrapper').removeClass("full-height");
                $('.main-footer').removeClass("absolute");
            }
        }
</script>

